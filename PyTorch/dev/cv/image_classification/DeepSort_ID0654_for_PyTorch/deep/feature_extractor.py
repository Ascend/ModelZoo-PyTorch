import torch
import torchvision.transforms as transforms
import numpy as np
import cv2
import logging

from .model import Net
import torch.npu
import os
NPU_CALCULATE_DEVICE = 0
if os.getenv('NPU_CALCULATE_DEVICE') and str.isdigit(os.getenv('NPU_CALCULATE_DEVICE')):
    NPU_CALCULATE_DEVICE = int(os.getenv('NPU_CALCULATE_DEVICE'))
if torch.npu.current_device() != NPU_CALCULATE_DEVICE:
    torch.npu.set_device(f'npu:{NPU_CALCULATE_DEVICE}')

class Extractor(object):
    #def __init__(self, model_path, use_cuda=True):
    def __init__(self, model_path, use_npu=True):
        self.net = Net(reid=True)
        #self.device = "cuda" if torch.cuda.is_available() and use_cuda else "cpu"
        self.device = "npu" if torch.npu.is_available() and use_npu else "cpu"
        state_dict = torch.load(model_path, map_location=f'npu:{NPU_CALCULATE_DEVICE}')['net_dict']
        self.net.load_state_dict(state_dict)
        logger = logging.getLogger("root.tracker")
        logger.info("Loading weights from {}... Done!".format(model_path))
        self.net.to(f'npu:{NPU_CALCULATE_DEVICE}')
        self.size = (64, 128)
        self.norm = transforms.Compose([
            transforms.ToTensor(),
            transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225]),
        ])
        


    def _preprocess(self, im_crops):
        """
        TODO:
            1. to float with scale from 0 to 1
            2. resize to (64, 128) as Market1501 dataset did
            3. concatenate to a numpy array
            3. to torch Tensor
            4. normalize
        """
        def _resize(im, size):
            return cv2.resize(im.astype(np.float32)/255., size)

        im_batch = torch.cat([self.norm(_resize(im, self.size)).unsqueeze(0) for im in im_crops], dim=0).float()
        return im_batch


    def __call__(self, im_crops):
        im_batch = self._preprocess(im_crops)
        with torch.no_grad():
            im_batch = im_batch.to(f'npu:{NPU_CALCULATE_DEVICE}')
            features = self.net(im_batch)
        return features.cpu().numpy()


