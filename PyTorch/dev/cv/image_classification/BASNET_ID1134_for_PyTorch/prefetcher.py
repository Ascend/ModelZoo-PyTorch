import torch

class DataPrefetcher():
    def __init__(self, loader, steam=None):
        self.loader = iter(loader)
        self.stream = torch.npu.Stream()
        # With Amp, it isn't necessary to manually convert data to half.
        # if args.fp16:
        #     self.mean = self.mean.half()
        #     self.std = self.std.half()
        self.preload()

    def preload(self):
        try:
            self.next_input,self.next_label = next(self.loader)
        except StopIteration:
            self.next_input = None
            self.next_label = None
            return
        with torch.npu.stream(self.stream):
            self.next_input = self.next_input.npu(non_blocking=True)
            self.next_label = self.next_label.npu(non_blocking=True)

            # With Amp, it isn't necessary to manually convert data to half.
            # if args.fp16:
            #     self.next_input = self.next_input.half()
            # else:
            #     self.next_input = self.next_input.float()

    def next(self):
        torch.npu.current_stream().wait_stream(self.stream)
        next_input = self.next_input
        next_label = self.next_label
        if next_label is not None:
            self.preload()       
        return next_input,next_label