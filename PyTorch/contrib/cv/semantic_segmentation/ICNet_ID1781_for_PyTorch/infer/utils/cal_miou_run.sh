image_width=2048
image_height=1024
# help message
if [[ $1 == --help || $1 == -h ]];then
    echo "usage:bash ./run.sh <args>"
    echo "parameter explain:
    --label_path        directory of dataset label, e.g. --label_path=../data/
    --output_path       path of the predict files that generated by the model, e.g. --output_path=../data/
    --image_width       set the image width,  default: --image_width=1024
    --image_height      set the image height, default: --image_height=512
    -h/--help           show help message
    "
    exit 1
fi

for para in "$@"
do
    if [[ $para == --label_path* ]];then
        label_path=`echo ${para#*=}`
    elif [[ $para == --output_path* ]];then
        output_path=`echo ${para#*=}`
    elif [[ $para == --image_width* ]];then
        image_width=`echo ${para#*=}`
    elif [[ $para == --image_height* ]];then
        image_height=`echo ${para#*=}`
    fi
done
if [[ $label_path  == "" ]];then
   echo "[Error] para \"image_path \" must be config"
   exit 1
fi

python3 cal_miou.py --label_path=$label_path \
                    --output_path=$output_path \
                    --image_width=$image_width \
                    --image_height=$image_height \

exit 0
